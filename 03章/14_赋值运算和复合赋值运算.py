age = 18
# 加法复合运算符
age += 1  # 等价于: age = age + 1
print(age)

price = 52
freight = 6
# 加法复合运算符
price += freight  # 等价于: price = price + freight
print(price)

# 减法复合运算符
age = 18
age -= 1  # 等价于: age = age - 1
print(age)

# 乘法复合运算符
price = 100
discount = 0.8
price *= discount  # 等价于: price = price * discount
print(price)