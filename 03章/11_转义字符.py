# 使用 \' 输出 '
print('在Python中，可以使用\'包裹一个字符串')

# 使用 \" 输出 "
print("在Python中，可以使用\"包裹一个字符串")

# 使用 \n 进行换行
print('注册会员需要以下信息：\n姓名\n年龄\n手机号')

# 使用 \\ 输出 \
print('D:\\nice')

# 使用\b 删除前一个字符  实现打字机效果
print('helloo\b')

# 使用\r 删除前一个字符  实现进度条效果
print('32%\r98%')

# 使用 \t 表示水平制表符（让光标跳转到下一个制表位）
print('1234123412341234')
print('ab\tcd')
print('abc\td')
print('abcd\ta')