### 集合的“修改”操作
# 核心结论 ：集合没有下标，也不支持 replace 方法，因此没有专门用于“改”的方法。
# 替代方案 ：可以用 remove + add 的组合来达到“修改”的效果（即先删除旧元素，再添加新元素）。
# 步骤1：移除旧元素（20）
l11 = {10, 20, 30, 40, 50}
l11.remove(20)
# 步骤2：添加新元素（66）
l11.add(66)
print('remove + add 的组合来达到“修改”的效果', l11)

# 集合的访问方式
# 核心结论 ：由于集合没有下标，也不支持切片操作，所以集合不具备按位置访问的能力。
# 替代方案 ：通过成员运算符可以判断某个元素是否在集合中。
l12 = {10, 20, 30, 40, 50}
rrr = 10 in l12
sss = 10 not in l12
print('通过成员运算符可以判断某个元素是否在集合中。', rrr)
print('通过成员运算符可以判断某个元素是否在集合中。', sss)

print('\n\n====================集合的常用方法=================')
# difference() 作用 ：找出集合A中，不同于集合B的元素（集合A与集合B都不变，返回的是一个新的集合）。
ss1 = {10, 20, 30, 40, 50}
ss2 = {30, 40, 50, 60, 70}
result = ss1.difference(ss2)
print('原集合不变', ss1)  # {10, 20, 30, 40, 50}（原集合不变）
print('原集合不变', ss2)  # {30, 40, 50, 60, 70}（原集合不变）
print('找出集合A中，不同于集合B的元素',result)  # {10, 20}（新集合：s1 中有但 s2 中没有的元素）

# difference_update() 作用 ：从集合A中，删除集合B中存在的元素（集合A会被修改，集合B不会）。
ll1 = {10, 20, 30, 40, 50}
ll2 = {30, 40, 50, 60, 70}
ll1.difference_update(ll2)
print('\n从集合A中，删除集合B中存在的元素',ll1)  # {10, 20}（集合A被修改）
print('集合B不变', ll2)  # {30, 40, 50, 60, 70}（集合B不变）

# union() 作用 ：合并两个集合，集合A和集合B都不变，返回的是一个新的集合。
k1 = {10, 20, 30}
k2 = {30, 40, 50}
kResult = k1.union(k2)
print('\n原集合不变', k1)  # {10, 20, 30}（原集合不变）
print('原集合不变', k2)  # {30, 40, 50}（原集合不变）
print('新集合：合并后的结果，自动去重', kResult)  # {10, 20, 30, 40, 50}（新集合：合并后的结果，自动去重）

# issubset() 作用 ：判断集合A是否为集合B的子集。
# 规则：如果集合A中的所有元素都在集合B中，返回 True ，否则返回 False 。
# 返回值：布尔值。
s1 = {10, 20, 30, 40, 50}
s2 = {30, 40, 50, 60, 70}
s3 = {30, 40, 50}
result = s3.issubset(s1)
print('\n判断集合A是否为集合B的子集。', result)
# 分析：s3的所有元素（30, 40, 50）都在s1中，因此返回True

# issuperset() 作用 ：判断集合A是否是集合B的超集。
# 规则：如果集合A中包含了集合B中的所有元素，返回 True ，否则返回 False 。
# 返回值：布尔值。
kk1 = {10, 20, 30, 40, 50}
kk2 = {30, 40, 50, 60, 70}
kk3 = {30, 40, 50}
resultK = kk2.issuperset(kk1)
print('\n判断集合A是否是集合B的超集', resultK)  # False（s2 不包含 s1 的所有元素，例如 10 和 20）

# isdisjoint() 作用 ：判断集合A和集合B是否没有交集。
# 规则：如果没有交集，返回 True ；但凡有一个公共元素，就返回 False 。
# 返回值：布尔值。
kr1 = {10, 20, 30, 40, 50}
kr2 = {30, 40, 50, 60, 70}
kr3 = {80, 90}
resultKr = kr1.isdisjoint(kr2)
print(resultKr)  # False（kr1 和 kr2 有公共元素 30、40、50）

print('\n\n====================集合的数学运算=================')
set_a = {10, 20, 30, 40, 50, 60}
set_b = {40, 50, 60, 70, 80, 90}
print('集合a', set_a)
print('集合b', set_b)
# 并集 |
union_result = set_a | set_b
print('并集', union_result)  # {70, 40, 10, 80, 50, 20, 90, 60, 30}

# 交集 &
intersection_result = set_a & set_b
print('交集', intersection_result)  # {40, 50, 60}

# 差集 -
difference_result = set_b - set_a
print('差集', difference_result)  # {70, 80, 90}

# 对称差集 ^
symmetric_difference_result = set_b ^ set_a
print('对称差集', symmetric_difference_result)  # {10, 20, 30, 70, 80, 90}

